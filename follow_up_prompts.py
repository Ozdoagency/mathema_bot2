def get_next_valid_time(current_time: datetime) -> datetime:
    """
    Определяет следующее допустимое время для напоминания.
    Если текущее время между 21:00 и 09:00, переносит на следующий день в 9:00.
    """
    if current_time.hour >= 21 or current_time.hour < 9:
        next_day = current_time + timedelta(days=1)
        return next_day.replace(hour=9, minute=0, second=0, microsecond=0)
    return current_time